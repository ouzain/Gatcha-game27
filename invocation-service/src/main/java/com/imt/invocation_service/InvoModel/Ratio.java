package com.imt.invocation_service.InvoModel;

public class Ratio {

    private String stat;
    private double percent;

    public Ratio() {
    }

    public String getStat() {
        return stat;
    }

    public double getPercent() {
        return percent;
    }

    public void setStat(String stat) {
        this.stat = stat;
    }

    public void setPercent(double percent) {
        this.percent = percent;
    }
}
